    private void jButtonSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSubmitMouseClicked
        String sql = "insert into fee values (?,?,?)";
        try{
            pst = conn.prepareStatement(sql);
            SimpleDateFormat inputFormat=new SimpleDateFormat("yyyy-MM");
            java.util.Date d;
            d = inputFormat.parse(txtyear.getSelectedItem().toString()+"-"+txtmonth.getSelectedItem().toString());
            SimpleDateFormat outputFormat=new SimpleDateFormat("MMMM-yyyy");
            pst.setString(1,outputFormat.format(d));
            pst.setString(2,txtfeestatus.getSelectedItem().toString());
            pst.setInt(3,sid);
            int n = pst.executeUpdate();
            if(n > 0){
                this.dispose();
                JOptionPane.showMessageDialog(null,"You have successfully registered");
            }
            else{
                JOptionPane.showMessageDialog(null,JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_jButtonSubmitMouseClicked

