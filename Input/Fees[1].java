    private void FillYear() {
        ArrayList<String> years_tmp = new ArrayList<>();
        for(int years = 1980; years<=Calendar.getInstance().get(Calendar.YEAR); years++) {
            years_tmp.add(years+"");
        }
        txtyear.setModel(new DefaultComboBoxModel(years_tmp.toArray()));
    }
